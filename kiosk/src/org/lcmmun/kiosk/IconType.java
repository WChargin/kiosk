package org.lcmmun.kiosk;

/**
 * The various types of icons for a delegate.
 * 
 * @author William Chargin
 */
public enum IconType {

	/**
	 * Indicates that the delegate's icon should be a country flag.
	 */
	FLAG,

	/**
	 * Indicates that the delegate's icon should be a stylized solid color.
	 */
	COLOR,

	/**
	 * Indicates that the delegate's icon should be a user-selected image.
	 */
	IMAGE,

	/**
	 * Indicates that the delegate should have no icon.
	 */
	NONE;

}
